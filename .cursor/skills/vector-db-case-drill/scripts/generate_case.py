#!/usr/bin/env python3
"""Генератор случайного кейса для тренажёра выбора векторной БД.

LLM сам по себе рандомит плохо (просишь «случайное число» — колеблется между 42 и 97).
Поэтому случайность даёт этот скрипт (stdlib random по value-set'ам осей), а агент-ментор
уже разворачивает выбранные якоря в правдоподобный сценарий.

    python generate_case.py          # новый случайный кейс
    python generate_case.py --twist  # одна новая вводная (для смены условий внутри раунда)
"""
import argparse
import random

AXES = {
    "Объём": [
        "~50K векторов — небольшой индекс",
        "~2M векторов — средний",
        "~50M векторов — крупный",
        "~500M+ векторов — очень крупный",
    ],
    "Фильтрация": [
        "фильтры по метаданным не нужны",
        "редкие фильтры по одному полю",
        "почти каждый запрос с фильтром по метаданным",
        "жёсткая мультитенантность: filter по tenant на каждом запросе",
    ],
    "Нагрузка": [
        "единичные запросы — внутренний инструмент",
        "десятки RPS",
        "сотни–тысячи RPS с жёстким p99",
    ],
    "Обновления": [
        "статичный корпус, редкие переиндексации",
        "ежедневные батч-обновления",
        "near-real-time поток вставок и удалений",
    ],
    "DevOps": [
        "DevOps-команды нет — нужен managed/serverless",
        "маленькая команда, хотим один контейнер рядом с приложением",
        "есть платформенная команда, ок держать кластер в k8s",
    ],
    "Хостинг и данные": [
        "строго on-prem — данные нельзя выносить в облако",
        "можно managed-облако",
        "в стеке уже есть PostgreSQL",
    ],
    "Бюджет": [
        "минимальный — стартап / PoC",
        "средний",
        "не ограничение, важнее SLA и надёжность",
    ],
}


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--twist", action="store_true", help="одна новая вводная вместо целого кейса")
    args = ap.parse_args()

    if args.twist:
        axis = random.choice(list(AXES))
        print(f"НОВАЯ ВВОДНАЯ → {axis}: {random.choice(AXES[axis])}")
        return

    print("СЛУЧАЙНЫЕ ЯКОРЯ КЕЙСА (не показывай их участнику дословно — разверни в живой сценарий):")
    for axis, values in AXES.items():
        print(f"  - {axis}: {random.choice(values)}")


if __name__ == "__main__":
    main()
